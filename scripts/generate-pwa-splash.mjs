const BG = "#1c1917";

function buildSplashSvg(width, height) {
  const min = Math.min(width, height);
  const logoSize = Math.round(min * 0.22);
  const gap = Math.round(min * 0.035);
  const fontSize = Math.round(min * 0.048);
  const blockH = logoSize + gap + fontSize * 1.35;
  const topY = Math.round((height - blockH) / 2);
  const logoX = Math.round((width - logoSize) / 2);
  const textY = topY + logoSize + gap + Math.round(fontSize * 0.85);

  const logoInner = `
  <g transform="translate(${logoX},${topY}) scale(${logoSize / 32})">
    <g transform="translate(6,6) scale(0.833)" stroke="#fafaf9" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" fill="none">
      <path d="M19 14c1.49-1.46 3-3.21 3-5.5A5.5 5.5 0 0 0 16.5 3c-1.76 0-3 .5-4.5 2-1.5-1.5-2.74-2-4.5-2A5.5 5.5 0 0 0 2 8.5c0 2.3 1.5 4.05 3 5.5l7 7Z"/>
      <path d="M3.22 12H9.5l1.5-2.5 2 5 1.5-3 1.5 2H21"/>
    </g>
  </g>`;

  return `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" width="${width}" height="${height}" viewBox="0 0 ${width} ${height}">
  <rect width="100%" height="100%" fill="${BG}"/>
  ${logoInner}
  <text x="${width / 2}" y="${textY}" text-anchor="middle" fill="#fafaf9" font-family="system-ui, -apple-system, BlinkMacSystemFont, &quot;Segoe UI&quot;, sans-serif" font-size="${fontSize}" font-weight="600">UPGS Monitor</text>
</svg>`;
}

await mkdir(outDir, { recursive: true });

const entries = [];

for (const { file, width, height, media } of SPLASH_SCREENS) {
  const svg = buildSplashSvg(width, height);
  const pngPath = join(outDir, file);
  await sharp(Buffer.from(svg)).png().toFile(pngPath);
  console.log(`Generated public/splash/${file}`);
  entries.push({ url: `/splash/${file}`, media });
}

const ts = `/** Generated by scripts/generate-pwa-splash.mjs — do not edit by hand. */

export const pwaSplashStartupImages: { url: string; media: string }[] = ${JSON.stringify(entries, null, 2)};
`;

await writeFile(generatedTs, ts, "utf8");
